    <p class="header-description">The global trend of population aging is characterized by a constant increase in the share of elderly people in the population, which
      attributed to factors such as increased life expectancy and declining fertility rates. The challenges they face
      people include physical and cognitive decline, social isolation and financial insecurity. <br><br>In particular, the fall can
      significantly increase the risk of injuries, such as fractures and head injuries, which can lead to hospitalization,
      disability and loss of independence. The system consists of sensor nodes (eg camera, smart watch) that enable
      crash detection, web applications for administration and storage of key crash information, and nodes
      recipient of alerts (e.g. mobile application).</p>
      
